//
//Всяка буква трябва да е на отделен ред.
package Lesson17_Exercise;

public class Atoz {
  public static void main(String[] args)
  {
    for(char chr = 'A'; chr <= 'Z'; chr++)
    {
      System.out.println(chr);

    }
  }
}
